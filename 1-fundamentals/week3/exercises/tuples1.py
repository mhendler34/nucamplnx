# TUPLES ARE IMMUTABLE

#          -5 -4 -3 -2 -1
#           0, 1, 2, 3, 4
my_tuple = (1, 2, 3, 4, 5)
# ACCESSING TUPLES

print(my_tuple[0] + my_tuple[1])
print(my_tuple[-1] + my_tuple[-2])
print(my_tuple[-5])
print(my_tuple[-4])
print(my_tuple[-3])
print(my_tuple[-2])
print(my_tuple[-1])

# To Change a TUPLE change reassign to tuple

my_tuple = (5, 4, 3, 2, 1)
print()
